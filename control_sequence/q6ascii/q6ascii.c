#include<stdio.h>
void main()
{
	int i;
	char ch;
	printf("upper case alphabets are:\n");
	for(ch=65;ch<=90;ch++)
	{
	printf("%c",ch);
	printf(" ");
	}
	printf("\n");
	printf("lower case alphabets are:\n");
	for(ch=97;ch<=122;ch++)
	{
	printf("%c",ch);
	printf(" ");
	}

	printf("\n");
	printf(" the numbers from 0 to 9 are:\n");
	for(i=0;i<10;i++)
	{
		printf("%d",i);
	}
}

	







